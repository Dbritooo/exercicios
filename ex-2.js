// Exercício 2: Verificação de dia da semana
// Escreva um programa que solicita ao usuário um número de 1 a 7 representando um dia da semana e exibe o nome correspondente a esse dia.
//  Considere que 1 representa segunda-feira, 2 representa terça-feira e assim por diante.




let username = prompt("digite um numero da semana")

if (username == 1) {
        console.log("domingo")

}
if (username == 7) {
        console.log("sabado")

}
if (username == 6) {
        console.log("sexta")

}
if (username == 5) {
        console.log("quinta")

}
if (username == 4) {
        console.log("quarta")

}
if (username == 3) {
        console.log("terça")

}
if (username == 2) {
        console.log("segunda")

}